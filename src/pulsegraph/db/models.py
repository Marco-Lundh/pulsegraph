class Notification(Base):
    """A delivered (or pending) notification (ADR 0003/0016)."""

    __tablename__ = "notifications"
    __table_args__ = (
        UniqueConstraint("user_id", "dedup_key"),
        Index(
            "idx_notifications_user",
            "user_id",
            text("delivered_at DESC"),
        ),
    )

    id: Mapped[uuid.UUID] = _pk()
    user_id: Mapped[uuid.UUID] = mapped_column(
        ForeignKey("users.id", ondelete="CASCADE"), nullable=False
    )
    analysis_id: Mapped[uuid.UUID] = mapped_column(
        ForeignKey("analyses.id", ondelete="CASCADE"), nullable=False
    )
    channel: Mapped[enums.NotificationChannel] = mapped_column(
        _enum(enums.NotificationChannel, "notification_channel"),
        nullable=False,
        server_default=text("'dashboard'"),
    )
    dedup_key: Mapped[str] = mapped_column(Text, nullable=False)
    status: Mapped[enums.NotificationStatus] = mapped_column(
        _enum(enums.NotificationStatus, "notification_status"),
        nullable=False,
        server_default=text("'pending'"),
    )
    delivered_at: Mapped[datetime.datetime | None] = mapped_column(
        DateTime(timezone=True)
    )
    # Digest delivery retry count (ADR 0016). Incremented each time a
    # daily-digest push fails for this row; once it reaches
    # Settings.digest_max_attempts the row is dead-lettered (status set to
    # FAILED) instead of being retried forever. Unused for instant/
    # dashboard delivery, which never leaves a row PENDING.
    attempts: Mapped[int] = mapped_column(
        Integer, nullable=False, server_default=text("0")
    )


